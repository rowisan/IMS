package com.rowi.lms.modle.vdo;

/**
 *
 * @author ROSHAN
 */
public class OtherChargeVDO {

    private String customer;
    private String cpu;
    private String charge;
    private String remark;
    private String amount;
    private String minExam;
    private String minTrial;
    private String chargeType;
    private String user;
    private String branch;

    public OtherChargeVDO() {
    }

    public String getCustomer() {
        return customer;
    }

    public void setCustomer(String customer) {
        this.customer = customer;
    }

    public String getCpu() {
        return cpu;
    }

    public void setCpu(String cpu) {
        this.cpu = cpu;
    }

    public String getCharge() {
        return charge;
    }

    public void setCharge(String charge) {
        this.charge = charge;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getMinExam() {
        return minExam;
    }

    public void setMinExam(String minExam) {
        this.minExam = minExam;
    }

    public String getMinTrial() {
        return minTrial;
    }

    public void setMinTrial(String minTrial) {
        this.minTrial = minTrial;
    }

    public String getChargeType() {
        return chargeType;
    }

    public void setChargeType(String chargeType) {
        this.chargeType = chargeType;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

}
